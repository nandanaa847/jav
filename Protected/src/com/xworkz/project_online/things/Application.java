package com.xworkz.project_online.things;

public class Application {

        protected String appname;
		protected String type;
		protected float rating;
		protected String use;
		protected String review;
		protected String creatorname;
		protected int releasedate;
		protected String country;
		protected String termsandconditions;
		protected int size;
		
		public Application() {
			super();
			
		}
		public Application(String appname , String type , float rating , String use , String review , String creatorname , int releasedate , String country , String termsandconditions , int size) {
			
			this.appname = appname;
			this.type = type;
			this.rating = rating;
			this.use = use;
			this.review = review;
			this.creatorname = creatorname;
			this.releasedate = releasedate;
			this.country = country;
			this.termsandconditions = termsandconditions;
			this.size = size;
			
			
		}
		
		

		
}
	

